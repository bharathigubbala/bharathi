
public class Swaping 
{

	public static void main(String[] args)
	{
	int x=29,y=40,temp;
System.out.println("Before swapping the  Two numbers");
System.out.println("x="+x);
System.out.println("y="+y);
temp=x;
x=y;		
y=temp;
System.out.println("After Swapping the Two numbers are:");
System.out.println("x="+x);
System.out.println("y="+y);

}
}
